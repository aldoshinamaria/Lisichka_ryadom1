const FEAR_HINT = /страшно|боюсь|страх|ужас|опасно|угроз|угрожа/i;

function needsSafetyTail(type, text) {
  if (type === 'bullying' || type === 'risk') return false;
  return FEAR_HINT.test(text);
}

const SAFETY_TAIL = ['Ты сейчас в безопасности?', 'Хочешь, я помогу позвать взрослого?'];

/**
 * @param {string} raw
 * @returns {'risk'|'bullying'|'school'|'closed'|'conflict'|'emotion'|'default'}
 */
export function classifyUserMessage(raw) {
  const t = raw.trim().toLowerCase();
  if (!t) return 'default';

  if (
    /суицид|самоубий|покончить с собой|не хочу жить|хочу умереть|выпрыг|повеш|убить себя|зарезаться|порезаться|ухожу из жизни|нет смысла жить/.test(t)
  ) {
    return 'risk';
  }
  if (/убьют|убьёт|убьет|угрожа|угроз|изнасил|насилие|с ножом|убьют меня|убить меня/.test(t)) {
    return 'risk';
  }

  if (
    /травл|буллинг|издева|дразнят|обзыва|унижают|насмехают|выгоняют|бьют меня|пинают|запирают|выкладывают в сеть/.test(t) ||
    /постоянно.*(обижают|дразнят|бьют)|каждый день.*(бьют|дразнят|обижают)/.test(t)
  ) {
    return 'bullying';
  }

  if (
    /школ|урок|учител|училк|класс|дневник|контрольн|на урок|в школу|не хочу идти|не пойду в школ|прогуливаю|боюсь.*школ|в школе плохо/.test(t)
  ) {
    return 'school';
  }

  if (
    /не хочу (говорить|рассказывать|это обсуждать)|не скажу|молчу|не буду писать|не спрашивай|не хочу об этом/.test(t)
  ) {
    return 'closed';
  }
  if (t.length <= 8 && !/школ|обижают|груст|страшно|тревог|помощ|плохо|боюсь/.test(t)) {
    return 'closed';
  }

  if (/конфликт|ссор|поссор|обидел|обижают|драк|подрал|поссорились/.test(t)) {
    return 'conflict';
  }

  if (
    /груст|тоск|одинок|тревож|страшно|боюсь|плохо|тяжел|тяжко|плачу|выговор|нужна помощь|помоги|не могу один|не могу справ|депресс|тревога/.test(
      t
    )
  ) {
    return 'emotion';
  }

  return 'default';
}

/**
 * Цепочка ответов Лисички после фразы ожидания (каждый элемент — отдельное сообщение в чате).
 * @param {string} userText
 */
export function getFoxFollowUpLines(userText) {
  const type = classifyUserMessage(userText);
  const lines = [...SEQUENCES[type]];
  if (needsSafetyTail(type, userText)) {
    lines.push(...SAFETY_TAIL);
  }
  return lines;
}

/** Сколько молчать после ответа Лисички, чтобы мягко спросить снова (3 минуты). */
