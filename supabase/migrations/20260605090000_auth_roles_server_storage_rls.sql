create table if not exists public.alerts (
  id bigint generated by default as identity primary key,
  student_id uuid references public.students(id) on delete set null,
  case_id text,
  message_id text references public.messages(id) on delete set null,
  alert_type text not null,
  status text not null default 'new' check (status in ('new', 'seen', 'in_progress', 'closed')),
  summary_for_adult text,
  source text not null default 'client',
  handled_by uuid references public.staff_profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

